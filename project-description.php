		<div class="projects-project-title">
			Strengthening Urban Education, research and university-city linkage through the Habitat Partner University Initiative
		</div>
		<div class="projects-project-description">
			Given the pace of change in cities, decision makers often lack timely and relevant data and sufficient understanding of the issues their urban centres face, which often leads to poorly thought out, and ultimately unsuccessful, policies. One obvious way to solve this deficiency is to leverage the significant capacity residing in universities to support decision making as well as prepare, and provide continued education to, decision makers. Yet a coordinated approach to develop the capacities of future and current urban leaders, to connect research with practice and to build the capacities of universities and researchers in is lacking, particularly in developing countries. The aim of the project Strengthening Urban Education, research and university-city linkages through the Habitat Partner University Initiative is to harness the strengths of universities and promote cooperation in the areas of education, research, professional development and policy advice in order to develop strong university-city collaborations on sustainable urban development.	
